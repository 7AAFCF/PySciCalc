import math

class Calculator:

    def __init__(self):
        pass

    def add(self, x, y):
        return x + y

    def subtract(self, x, y):
        return x - y

    def multilpy(self, x, y):
        return x*y

    def divide(self, x, y):
        return x / y

    def square(self, x):
        return x * x

    def square_root(self, x):
        return math.sqrt(x)

